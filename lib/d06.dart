import 'dart:convert';

int run() {
  var ls = LineSplitter();
  final map = ls.convert(inp);

  // part 1
  // return solve(map);

  final size = map.length;

  var loops = 0;

  for (var i = 0; i < size; i++) {
    for (var j = 0; j < size; j++) {
      var perm = map.toList();
      final String p = perm[i][j];
      if (!(p == "^" || p == "v" || p == "<" || p == ">")) {
        var whereIsMyArray = perm[i].split("");
        whereIsMyArray.replaceRange(j, j+1, ["#"]);
        perm[i] = whereIsMyArray.join();
      }

      if (solve(perm) == -1) {
        loops++;
      }
    }
  }

  return loops;
}

int solve(map) {
  Set<(int x, int y)> obs = {};
  Set<(int x, int y)> vis = {};
  (int x, int y, String d) pos = (-1, -1, "");

  final size = map.length;
  for (var i = 0; i < size; i++) {
    for (var j = 0; j < size; j++) {
      final String p = map[i][j];
      if (p == "#") {
        obs.add((j, i));
      } else if (p == "^" || p == "v" || p == "<" || p == ">") {
        vis.add((j, i));
        pos = (j, i, p);
      }
    }
  }

  (int, int, String)? move((int, int, String) p) {
    var x = p.$1 + switch (p.$3) {
      ">" => 1,
      "<" => -1,
      _ => 0,
    };

    var y = p.$2 + switch (p.$3) {
      "v" => 1,
      "^" => -1,
      _ => 0,
    };

    if (x < 0 || x >= size || y < 0 || y >= size) {
      return null;
    }

    var d = p.$3;

    if (obs.contains((x, y))) {
      switch (d) {
        case "^":
          d = ">";
          y = p.$2;
          break;
        case ">":
          d = "v";
          x = p.$1;
          break;
        case "v":
          d = "<";
          y = p.$2;
          break;
        default:
          d = "^";
          x = p.$1;
      }
    }

    return (x, y, d);
  }

  var dirChange = 0;

  while (true) {
    final next = move(pos);

    if (next == null) {
      return vis.length;
    }

    vis.add((next.$1, next.$2));

    if (pos.$3 != next.$3) {
      dirChange++;
    }

    if (dirChange > vis.length) {
      return -1;
    }

    pos = next;
  }
}

